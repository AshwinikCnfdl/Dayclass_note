package Java_classes;

public class Op_overloading {


    public static void main(String[] args){

        System.out.println( 20 + 50);//70
        System.out.println(20 + "30" + 20);// "2030"+20 = 203020
        System.out.println("30" + 20 + 10); // 302010
        System.out.println(10+20+"30"); // 30+30 = 3030
    }
}
